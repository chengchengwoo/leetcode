/**
 * Definition for singly-linked list.
 * struct ListNode {
 *     int val;
 *     ListNode *next;
 *     ListNode(int x) : val(x), next(NULL) {}
 * };
 */
class Solution {
public:
    bool hasCycle(ListNode *head) {
        //return usingSet(head);
        return fastSlowPointer(head);
    }
    // T: O(n) S: O(1)
    bool fastSlowPointer(ListNode* head) {
        ListNode* fast = head;
        ListNode* slow = head;
        while (fast && fast->next) {
            fast = fast->next->next;
            slow = slow->next;
            if (fast == slow) {
                return true;
            }
        }
        return false;
    }
    // T: O(n) S: O(n)
    bool usingSet(ListNode* head) {
        unordered_set<ListNode*> s;
        for (; head; head = head->next) {
            // exists
            if (!s.insert(head).second) {
                return true;
            }
        }
        return false;
    }
};

